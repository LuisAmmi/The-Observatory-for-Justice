

  $('#covel').click(function(){
        if ($(this).checked = true)
          {
            $('#MarkCovelscase').css({"background-color": "#fff"});
            $('#article1').scrollTop('#MarkCovelscase');
          }
        else if ($(this).checked = false)
         {
          $('#MarkCovelscase').css({"background-color": "transparent"});
         }
    });
